    public boolean loadComboBox() {
        jComboBox1.removeAllItems();
        for (Map.Entry<Object, Object> entry : settings.entrySet()) {
            String key = (String) entry.getKey();
            jComboBox1.addItem(key);
        }
        return true;
    }

    public boolean loadLangData() {
        try {
            FileInputStream fis;
            fis = new FileInputStream(lang_path);
            settings.load(fis);
            fis.close();
        } catch (IOException e) {
            System.out.println("Cannot find: " + lang_path);
            return false;
        }
        return true;
    }

