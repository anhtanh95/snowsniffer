    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = jTextField2.getText();
            String value = jTextField3.getText();
            if (settings.containsKey(name)) {
                //UPDATE
                settings.setProperty(name, value);
            } else {
                //ADD NEW
                settings.put(name, value);
            }
            settings.store(new FileOutputStream(lang_path), null);
            loadLangData();
            loadComboBox();
        } catch (IOException ex) {
            System.out.println("Cannot find: " + lang_path);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Object selected = jComboBox1.getSelectedItem();
            settings.remove(selected);
            settings.store(new FileOutputStream(lang_path), null);
            loadLangData();
            loadComboBox();
        } catch (IOException ex) {
            System.out.println("Cannot find: " + lang_path);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jComboBox1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jComboBox1PropertyChange
        boolean selectRow = jComboBox1.getSelectedIndex() > -1;
        Object selected = jComboBox1.getSelectedItem();
        if (selectRow) {
            jTextField2.setText(selected.toString());
            jTextField3.setText(settings.get(selected).toString());
        }
    }//GEN-LAST:event_jComboBox1PropertyChange

